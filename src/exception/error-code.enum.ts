  /* 404 NOT_FOUND : Resource 를 찾을 수 없음 */
  [ErrorCode.NOT_FOUND]: HttpStatus.NOT_FOUND,
  [ErrorCode.NOT_FOUND_USER]: HttpStatus.NOT_FOUND,
  [ErrorCode.NOT_FOUND_TENANT]: HttpStatus.NOT_FOUND,
